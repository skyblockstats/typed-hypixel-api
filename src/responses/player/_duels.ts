/**
 * How inventory layouts are formatted in duels. Note that some inventories are
 * a little different, see `DuelsInventoryAlternateLayout` for that.
 * 
 * The key is a number representing the inventory position; 0-8 are the hotbar.
 * The value is formatted as {item id},{damage},{amount},{extra}.
 * The extra will either be empty, or have enchantments formatted as
 * `e:DIG_SPEED` or `e:DAMAGE_ALLe:FIRE_ASPECT` if there's multiple.

 */
export interface DuelsInventoryLayout {
	[key: number]: `${number},${number},${number},${string}`
}

/**
 * Sometimes layouts are formatted like this for some reason. The value can
 * either be a number id (like iron_sword) or something like `layout_scout2`. I
 * don't know what the latter means.
 */
export interface DuelsInventoryAlternateLayout {
	[key: number]: string | `layout_${string}${number}`
}
